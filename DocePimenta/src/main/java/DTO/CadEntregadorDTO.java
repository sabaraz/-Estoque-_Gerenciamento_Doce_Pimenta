package DTO;

public class CadEntregadorDTO {
    
    private String nomeEntregador;
    private String telefoneEntregador;
    private String empresaTrabalha;
    
    //getter and setter

    public String getNomeEntregador() {
        return nomeEntregador;
    }

    public void setNomeEntregador(String nomeEntregador) {
        this.nomeEntregador = nomeEntregador;
    }

    public String getTelefoneEntregador() {
        return telefoneEntregador;
    }

    public void setTelefoneEntregador(String telefoneEntregador) {
        this.telefoneEntregador = telefoneEntregador;
    }

    public String getEmpresaTrabalha() {
        return empresaTrabalha;
    }

    public void setEmpresaTrabalha(String empresaTrabalha) {
        this.empresaTrabalha = empresaTrabalha;
    }
    
    
    
}
